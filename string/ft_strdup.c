#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*new;
	int		ct;
	int		i;

	ct = 0;
	while (s1[ct])
		ct++;
	new = (char *)malloc(sizeof(char) * (ct + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
